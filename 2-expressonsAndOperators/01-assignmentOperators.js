//* ASSIGNMENT OPERATOR

let r = 5;

//* ADDITION ASSIGNMENT

r += 1; //? same as writing r = r + 1

//* SUBTRACTION ASSIGNEMT

r -= 1; //? same as writing r = r - 1

//* MULTIPLICATION ASSIGNMENT

r *= 1; //? same as writing r = r * 1

//* DIVISION ASSIGNMENT

r /= 1; //? same as writing r = r / 1

//* MODULUS OPERATOR - this is the whole number remainder

r %= 1; //? same as writing r = r % 1

//* EXPONENTIAL ASSIGNMENT OPERATOR

r **= 1; //? same as writing r = r** 1